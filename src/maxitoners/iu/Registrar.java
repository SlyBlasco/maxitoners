    private void cargarDatos() {
        DefaultTableModel tabla = (DefaultTableModel) tblRegistros.getModel();
        tabla.setRowCount(0);
        ImpresoraDAO impresoraDao = new ImpresoraDAO();

        try {
            List<Impresora> impresoras = impresoraDao.getAllImpresoras();
            for (Impresora i : impresoras) {
                tabla.addRow(new Object[]{
                    i.getIdCliente(),
                    i.getMarca() + " " + i.getModelo(),
                    i.getNumeroSerie(),
                    "REGISTRAR"
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la tabla.");
        }
    }

