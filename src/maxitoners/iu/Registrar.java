    private void tblRegistrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRegistrosMouseClicked
        int indexCol = tblRegistros.getSelectedColumn();
        int indexRow = tblRegistros.getSelectedRow();
        TableModel model = tblRegistros.getModel();

        if (indexCol == 3) {
            String noSerie = (String) model.getValueAt(indexRow, 2);
            ImpresoraDAO impresoraDao = new ImpresoraDAO();
            try {
                // Obtener la impresora seleccionada basada en el cliente (indexRow corresponde al cliente)
                Impresora impresoraSeleccionada = impresoraDao.getByNoSerieIND(noSerie);
                Registrador ventanaRegistrador = new Registrador();
                ventanaRegistrador.setIdImpresora(impresoraSeleccionada.getId());
                ventanaRegistrador.setModeloImpresora(impresoraSeleccionada.getMarca() + " " + impresoraSeleccionada.getModelo());
                ventanaRegistrador.setNoSerieImpresora(impresoraSeleccionada.getNumeroSerie());
                ventanaRegistrador.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }

    }//GEN-LAST:event_tblRegistrosMouseClicked

