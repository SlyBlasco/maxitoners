    private void txfBuscadorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txfBuscadorKeyReleased
        String filtro = txfBuscador.getText();

        DefaultTableModel tablaClientes = (DefaultTableModel) tblCliente.getModel();
        ClienteDAO clienteDao = new ClienteDAO();
        tablaClientes.setRowCount(0);

        try {
            List<Cliente> clientes = clienteDao.getByName(filtro);
            for (Cliente cliente : clientes) {
                tablaClientes.addRow(new Object[]{
                    cliente.getId(),
                    cliente.getNombre(),
                    cliente.getDireccion(),
                    cliente.getRfc()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la tabla.");
        }
        
        DefaultTableModel tablaImpresoras = (DefaultTableModel) tblImpresora.getModel();
        ImpresoraDAO impresoraDao = new ImpresoraDAO();
        tablaImpresoras.setRowCount(0);
        
        try {
            List<Impresora> impresoras = impresoraDao.getByMarca(filtro);
            for(Impresora impresora : impresoras){
                tablaImpresoras.addRow(new Object[]{
                    impresora.getId(),
                    impresora.getMarca(),
                    impresora.getModelo(),
                    impresora.getNumeroSerie()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la tabla.");
        }
    }//GEN-LAST:event_txfBuscadorKeyReleased

