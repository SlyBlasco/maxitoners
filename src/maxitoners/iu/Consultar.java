    public void cargarDatos() {
        DefaultTableModel tablaClientes = (DefaultTableModel) tblCliente.getModel();
        ClienteDAO clienteDao = new ClienteDAO();
        tablaClientes.setRowCount(0);

        try {
            List<Cliente> clientes = clienteDao.getAllClientes();
            for (Cliente cliente : clientes) {
                tablaClientes.addRow(new Object[]{
                    cliente.getId(),
                    cliente.getNombre(),
                    cliente.getDireccion(),
                    cliente.getRfc()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la tabla.");
        }

        DefaultTableModel tablaImpresoras = (DefaultTableModel) tblImpresora.getModel();
        ImpresoraDAO impresoraDao = new ImpresoraDAO();
        tablaImpresoras.setRowCount(0);

        try {
            List<Impresora> impresoras = impresoraDao.getAllImpresoras();
            for (Impresora impresora : impresoras) {
                tablaImpresoras.addRow(new Object[]{
                    impresora.getId(),
                    impresora.getMarca(),
                    impresora.getModelo(),
                    impresora.getNumeroSerie()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la tabla.");
        }
        
        DefaultTableModel tablaRegistros = (DefaultTableModel) tblRegistros.getModel();
        RegistroDAO registroDAO = new RegistroDAO();
        tablaRegistros.setRowCount(0);
        
        try {
            List<Registro> registros = registroDAO.getAllRegistros();
            for(Registro registro : registros){
                tablaRegistros.addRow(new Object[]{
                    registro.getId(),
                    registro.getMarcaImpresora() + " " + registro.getModeloImpresora(),
                    registro.getNombreCliente(),
                    registro.getNumImpresiones(),
                    registro.getFecha()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la tabla.");
        }

    }
    
